# ruby 3-data-types.rb
# <== Strings ==>

name = "Mike"

# <== Integer numbers ==>

age = 45

# <== Floating point numbers ==>

gpa = 3.2458379689

# <== Boolean ==>

isMale  = true
isShort = false
flaws   = nil   # Like 'null' in other languages (No value)

# <== Outputing ==>

puts ("Name   : " + name)
puts ("Age    : " + age.to_s)
puts ("GPA    : " + gpa.to_s)
puts ("Male?  : " + isMale.to_s)
puts ("Short? : " + isShort.to_s)
puts ("Flaws? : " + flaws.to_s)